/**
 * Book My Stay App
 *
 * This class represents the entry point of the Hotel Booking Application.
 * It demonstrates how a Java program starts execution and displays
 * a welcome message along with application details.
 *
 * @author Shreya
 * @version 1.0
 */

public class BookMyStayApp {

    /**
     * Main method - Entry point of the application
     * JVM starts execution from here
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("====================================");
        System.out.println("   Welcome to Book My Stay App   ");
        System.out.println("====================================");

        // Application details
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: v1.0");

        // Exit message
        System.out.println("\nApplication started successfully!");
        System.out.println("Thank you for using Book My Stay App.");
    }
}